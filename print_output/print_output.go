package printoutput

import "fmt"

func PrintOutput() {
	//fmt.Printf()
	name := "Dung"
	fmt.Print("Hello, ")
	fmt.Print("World!")
	fmt.Print("Name:", name)

	// fmt.Println()
	fmt.Println()
	fmt.Println("Hello")
	fmt.Println("World!")
	fmt.Println("Name:", name)

	//fmt.Printf()
	msg := "Hello, world"
	fmt.Printf("%s", msg)
	// float : %g
	// int : %d
	// bool : %t
	// string : %s

	// print and println
	println()
	print("Xin chao")
	println()
	println("Xin chao")
}
