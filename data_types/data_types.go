package data_types

import "fmt"

func DataTypes() {
	// int
	var id, age int
	id = 1
	age = 1
	fmt.Println(id, age)
	// float
	var price float32
	price = 2.34
	fmt.Println(price)

	//string
	var msg = "Hello, world!"
	fmt.Println(msg)

	//bool
	var isLoading bool

	isLoading = true

	fmt.Println(isLoading)

}
